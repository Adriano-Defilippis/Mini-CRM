     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function showMore(Request $request)
    {

      $page = $request -> page;
      $max = $page * 10;
      $min = $max - 9;

      $companies = Company::whereBetween('id', [$min, $max])->get();

      return json_decode(
        $companies
      );
    }

